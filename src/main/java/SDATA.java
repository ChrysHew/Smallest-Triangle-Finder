public class SDATA {
    public int infoX;
    public int infoY;

    public SDATA(int x,int y)
    {
        this.infoX=x;
        this.infoY=y;
    }

    public int getInfoX() {
        return infoX;
    }

    public int getInfoY() {
        return infoY;
    }

    public void setInfoX(int infoX) {
        this.infoX = infoX;
    }

    public void setInfoY(int infoY) {
        this.infoY = infoY;
    }
}